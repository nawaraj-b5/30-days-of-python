#Find the length of the text python and convert the value to float and convert it to string
length_of_python = ( len('python') )
length_of_python_in_float = float(length_of_python)
length_of_python_in_string = str( length_of_python )
print ('Length of the python in integer is {}, float is {}, and string is {} '.format(length_of_python,length_of_python_in_float,length_of_python_in_string))
