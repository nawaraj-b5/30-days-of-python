#There is no 'on' in both dragon and python
dragon = 'dragon'
python ='python'
on_not_in_dragon = 'on' not in dragon
on_not_in_python = 'on' not in python
print("The statement that 'on' is not found in dragon is  :",on_not_in_dragon  )
print("The statement that 'on' is not found in python is  :",on_not_in_python  )
print("The statement that 'on' is not found in both dragon and python is  :",on_not_in_python and on_not_in_dragon  )