#Declare 5 as num_one and 4 as num_two
num_one = 5
num_two = 4
variable_total = num_one + num_two          #Add num_one and num_two and assign the value to a variable _total
variable_diff = num_two - num_one           #Subtract num_two from num_one and assign the value to a variable _diff
variable_product = num_two * num_one       #Multiply num_two and num_one and assign the value to a variable _product
variable_division = num_two / num_one        #Divide num_one by num_two and assign the value to a variable _division
variable_reminder = num_two % num_one        #Use modulus division to find num_two divided by num_one and assign the value to a variable _remainder
variable_exp = num_one ** num_two           #Calculate num_one to the power of num_two and assign the value to a variable _exp
variable_floor_division = num_one // num_two   #Find floor division of num_one by num_two and assign the value to a variable _floor_division
print( "Total : {}".format(variable_total) )
print( "Difference : {}".format(variable_diff) )
print( "Product : {}".format(variable_product) )
print( "Division : {}".format(variable_division) )
print( "Reminder : {}".format(variable_reminder) )
print( "Exponent : {}".format(variable_exp) )
